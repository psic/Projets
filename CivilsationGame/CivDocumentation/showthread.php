		<div id="post_message_161215">
			
			In response to the requests I received earlier this month with regards to how &quot;culture-flipping&quot; works, I sat down with Soren, the lead programmer on Civ III, and he explained which factors influence the probability of a city &quot;flipping&quot; and what the relative weight of each factor was.  <br />
<br />
The base values used to determine the chance of city flipping are as follows:<br />
<br />
A) The number of foreign nationals in the city in question (resisters are counted twice), and<br />
<br />
B) The number of the 21-tile city-radius squares of the city in question that fall inside your cultural borders.<br />
<br />
<br />
These numbers are then further modified by a variety of factors, applied multiplicatively. Here those are, in order of importance:<br />
<br />
<br />
1) The ratio of distances to the respective capitals of both cities.    Basically, if you're closer to your capital than the other city is to its capital, you've got a better chance of getting a flip.<br />
<br />
2) The ratio of total culture points of both civs.  Obviously, the better your culture is versus the opponent civ, the better your chance of getting a flip are.<br />
<br />
3) I didn't even know this, but apparently each city has a &quot;memory&quot; and remembers the total amount of culture generated by any civ who has ever occupied it.  This is the 3rd most important factor, because if the &quot;attacking&quot; civ has more historical culture in the city than the &quot;defender&quot;, the chance of that city flipping to the attacker are doubled.  This is one reason that conquered cities often flip back to their previous owners.<br />
<br />
4) Civil Disorder in a city doubles the chance of that city flipping.<br />
<br />
5) We Love the King (or whatever) Day in a city halves the chance of that city flipping.<br />
<br />
6) Lastly, the number of land-based combat units (e.g., any unit with at least 1 point of offensive and defensive capability) in the city in question are subtracted.  This factor is relatively low on the totem pole and this shows you why cities can flip even with huge militias garrisoned in them.<br />
<br />
<br />
Hope this helps.<br />
