			I'm ecstatic to see Firaxis &quot;come clean&quot; on the city flip factors.  A couple of things are missing, so I guess we can assume they play no part, despite what some of us had assumed:<br />
<br />
<u>Happy citizens</u> (besides the potential for a WLTKD);<br />
<br />
<u>Courthouse?</u>  I was sure that the Civilopedia or manual said that a courthouse helped against flips, but I can't confirm that at work!  <img src="http://forums.civfanatics.com/images/smilies/wink.gif" border="0" alt="" title="Wink" class="inlineimg" /><br />
<br />
<u>Other improvements, like Temples or Marketplaces?</u>  I guess their only effect is to start slowly building your culture in the city towards that &quot;memory&quot; number it had accumulated with its former civ.  My conquests in the Late Industrial/Modern age often have thousands of points of accumulated culture, so <b>a temple would do absolutely nothing to decrease the chance of the city flipping back to the enemy!</b>  That's a huge surprise.<br />
<br />
<u>Proximity of the Forbidden Palace?</u>  The Civilopedia states that the FP &quot;confers all of the benefits of a second place&quot;.  Should we assume that the distance used for the flipping calculation is the shortest distance to either of your palaces?  That can be a huge deal.<br />
<br />
<u>Big culture-producing <i>cities</i> that are nearby?</u>  A lot of people assumed that a cultural dynamo at the border would put pressure on the cities around it.  I'm glad to have that refuted!<br />
<br />
<u>Over-large garrisons?</u>  Some testing (trying to be careful about random seeds) had suggested that having a garrison that is larger than the pop of the city actually <i>increased</i> their chance of flipping!<br />
<br />
<br />
The Civilopedia states that the following factors increase the chance for city flipping:<br />
<br />
- Presence of foreign nationals in the city <br />
- Propaganda used against the city <br />
- Civil disorder<br />
- Nearby foreign territory <br />
- Proximity of the foreign capital <br />
<br />
I guess that needs to be amended adding the <b>doubled effect of foreign Resistors</b>, the <b>cultural memory effect</b>, and the effect of the <b>total cuture ratio between the two civs</b>?  Clarifying the &quot;nearby foreign territory&quot; one would also be helpful.<br />
<br />
<br />
Thanks again though!  <img src="http://forums.civfanatics.com/images/smilies/goodwork.gif" border="0" alt="" title="good job" class="inlineimg" />
